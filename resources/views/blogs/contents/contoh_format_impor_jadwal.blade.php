<p class="mt-2">Catatan:</p>
<ol>
  <li>Perhatikan nama sheet. Nama sheet haruslah <b>Sheet1</b>.</li>
  <li>Perhatikan kolom yang berwarna <span class="text-danger">merah</span>. Kolom berwarna merah tidak boleh diubah-ubah.</li>
  <li>Perhatikan kolom yang berwarna <span class="text-warning">kuning</span>. Kolom berwarna kuning adalah kolom yang dapat diubah dengan syarat dan ketentuan tertentu.</li>
  <li>Perhatikan kolom yang berwarna <span class="text-success">hijau</span>. Kolom berwarna hijau adalah kolom jam yang akan diisikan.</li>
  <li>Kolom putih tidak berpengaruh dalam prosesi impor, dan dapat digunakan sebagai sarana untuk mencatat.</li>
</ol>
<p class="mt-2">Ketentuan:</p>
<ol>
  <li>Kolom berwarna merah adalah tanggal, dan dalam kondisi apapun tidak boleh diganti termasuk bila dalam suatu bulan hanya terdapat 30 hari, atau 28 hari. Penggantian terhadap kolom ini tidak berakibat fatal bagi sistem, namun sistem akan tetap membaca data jam yang diberikan sesuai keadaan seperti diatas. Semisal bila anda mengganti kolom A4 dari angka 1 ke 3, sistem akan tetap membaca data di bawahnya sebagai data tanggal 1 Agustus 2020, tidak serta merta menjadi 3 Agustus 2020.</li>
  <li>Kolom kuning adalah data-data yang dapat anda isi sesuai ketentuan:</li>
  <ol>
    <li>Kolom B2 harus diisikan kode cabang yang valid. Bila kode cabang tidak valid maka data tidak akan masuk.</li>
    <li>Kolom B3 dan C3 harus diisikan bulan dan tahun yang valid secara valid. Bila tidak valid maka konsekuensi seperti poin diatas akan terjadi.</li>
    <li>Setiap kali anda ingin memasukkan sebuah data, anda wajib menuliskan <b>ID:</b> pada kolom A, diikuti dengan nomor ID Finger pada kolom C. Keduanya harus sejajar. Bila tulisan <b>ID:</b> tidak ada, atau nomor ID Finger tidak valid, maka sistem akan mengabaikan data.</li>
  </ol>
  <li>Kolom hijau adalah data jam jadwal yang dapat anda isi sesuai ketentuan:</li>
  <ol>
    <li>Harus diisikan 1 baris dibawah tulisan <b>ID:</b> dan nomor ID Finger.</li>
    <li>Format haruslah <b>'hh:mm</b> (sesuai standar <a href="https://id.wikipedia.org/wiki/ISO_8601#Waktu" target="_blank">ISO 8601</a>, dengan pengecualian <b>tanpa ada detik</b>). Contoh, pukul 9 pagi lebih 12 menit menjadi <b>'09:12</b>, pukul 1 siang lebih 44 menit menjadi <b>'13:44</b>.</li>
    <li>Tanda <b>'</b> harus ditambahkan di depan jam supaya format tidak bergeser karena Excel.</li>
    <li>Bila pada tanggal tertentu seorang karyawan libur / off / tidak masuk karena <b>penyebab apapun</b>, kolom dikosongkan saja, jangan ada tulisan apapun.</li>
  </ol>
</ol>   